const LOG = (message) =>
  console.log(`%c${message}`, 'color:white; background-color:black; font-size:1rem');
const ERROR = (message) =>
  console.log(`%c${message}`, 'color:red; background-color:black; font-size:1rem');
const WARN = (message) =>
  console.log(`%c${message}`, 'color:yellow; background-color:black; font-size:1rem');
const INFO = (message) =>
  console.log(`%c${message}`, 'color:cyan; background-color:black; font-size:1rem');
